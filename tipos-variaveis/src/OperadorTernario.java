public class OperadorTernario {
  public static void main(String[] args) {
    int a,b;
    a = 5;
    b = 6;

    String resultado = a == b ? "Verdadeiro" : "Falso";

    System.out.println(resultado);

    int c,d;
    c = 2;
    d = 5; 

    int resultado1 = c == d ? 1 : 0;

    System.out.println(resultado1);
  }
}
